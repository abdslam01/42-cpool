char	*ft_strupcase(char *str)
{
	int	i;
	int	is_low;

	i = 0;
	while (str[i])
	{
		is_low = str[i] >= 'a' && str[i] <= 'z';
		if (is_low)
			str[i] -= ('a' - 'A');
		i++;
	}
	return (str);
}
